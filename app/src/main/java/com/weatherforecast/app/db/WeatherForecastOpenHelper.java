package com.weatherforecast.app.db;

import android.content.Context;
import android.database.sqlite.SQLiteDatabase;
import android.database.sqlite.SQLiteOpenHelper;

/**
 * Created by chenmo on 2015/5/24.
 */
public class WeatherForecastOpenHelper  extends SQLiteOpenHelper {
    /*
    *    将一下三条建表语句定义成常量 然后在onCreate()方法中去执行创建
    * */

    /*
    * province 表建表语句
    * */
    public  static final  String CREATE_PROVINCE ="create table Province("
            + "id integer primary key autoincrement, "
            + "province_name text, "
            + "province_code text)";
    /*
    *     city表 建表语句
    * */

    public static  final  String CREATE_CITY = "create table City("
            + "id integer primary key autoincrement, "
            + "city_name text, "
            + "city_code text, "
            + "province_id integer)";

    /*
    *   Country表 建表语句
    * */

    public  static  final  String CREATE_COUNTRY = "create table Country("
            + "id integer primary key autoincrement, "
            + "country_name text, "
            + "country_code text, "
            + "city_id integer)";


    public WeatherForecastOpenHelper(Context context, String name, SQLiteDatabase.CursorFactory factory, int version) {
        super(context, name, factory, version);
    }

    @Override
    public void onCreate(SQLiteDatabase db) {

        db.execSQL(CREATE_PROVINCE); // 创建province表
        db.execSQL(CREATE_CITY);  // 创建 city表
        db.execSQL(CREATE_COUNTRY); // 创建 country表
    }
    @Override
    public void onUpgrade(SQLiteDatabase db, int oldVersion, int newVersion) {

    }
}


